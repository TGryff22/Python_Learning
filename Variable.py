# Variable = A container for a value (string, integer, float, boolean). Used to store data in a program. 

# Strings
topping = "pepperoni"
soda = "Mountain Dew"
dog = "Crash and Cinnamon"

print(f"I like {topping} on my pizza.")
print(f"I like {soda} with my pizza.")
print(f"My dog {dog} likes pizza too.")

print(" ")

# Integers
age = 100
score = 98
my_weight = 200

print(f"I am {age} years old.") 
print(f"I scored {score} in my final exam.")
print(f"I weigh {my_weight} pounds.")

print(" ")

# Floats
pi = 3.14
price = 19.99
distance = 3.5

print(f"The value of pi is approximately {pi}.")
print(f"The plate lunch is ${price}.")
print(f"We ran about {distance} miles today.")

print(" ")

# Booleans. Used comonly in if statements and loops.

is_student = True
is_hungry = False
is_green = True

print(f"Am i a student? {is_student}")
print(f"Am i hungry? {is_hungry}")
print(f"Is the sky green? {is_green}")